    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (!jTextField1.getText().isEmpty()) {

            for (Producto prod : Menu.lista) {
                if (prod.getCodigo() == Integer.parseInt(jTextField1.getText())) {
                    jTextField2.setText(prod.getDescripcion());
                    jTextField3.setText(prod.getPrecio() + "");
                    jComboBox1.setSelectedItem(prod.getRubro());
                    jTextField5.setText(prod.getStock() + "");
//                    this.jTextField2.setEditable(true);
//                    this.jTextField3.setEditable(true);
//                    this.jTextField5.setEditable(true);
                        break;
                } else {
                    JOptionPane.showMessageDialog(this, "No existe producto con el codigo indicado");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jComboBox1.setSelectedIndex(0);
                    jTextField5.setText("");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "No ingres√≤ ningun codigo");
            this.jTextField1.setText("");
            this.jTextField2.setText("");
            this.jTextField3.setText("");
            this.jTextField5.setText("");
            this.jComboBox1.setSelectedItem("");
            this.jTextField2.setEditable(false);
            this.jTextField3.setEditable(false);
            this.jTextField5.setEditable(false);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField5.getText().isEmpty() || jComboBox1.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Por favor completar todos los campos");
        } else {
            try {
                Producto prod = new Producto(Integer.parseInt(jTextField1.getText()), jTextField2.getText(), Double.parseDouble(jTextField3.getText()), Integer.parseInt(jTextField5.getText()), (Categoria) jComboBox1.getSelectedItem());
                Menu.lista.add(prod);
                JOptionPane.showMessageDialog(this, "Cambios guardados");
                this.jTextField1.setText("");
                this.jTextField2.setText("");
                this.jTextField3.setText("");
                this.jTextField5.setText("");
                this.jComboBox1.setSelectedItem("");
                this.jTextField1.setEditable(true);
                this.jTextField2.setEditable(false);
                this.jTextField3.setEditable(false);
                this.jTextField5.setEditable(false);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Para precio y stock solo colocar numeros");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField5.getText().isEmpty() || jComboBox1.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Por favor completar todos los campos antes de eliminar un producto");
        }
        for (Producto prod : Menu.lista) {
            if (prod.getCodigo() == Integer.parseInt(jTextField1.getText())) {
                Menu.lista.remove(prod);
                JOptionPane.showMessageDialog(this, "Producto eliminado");
                this.jTextField1.setText("");
                this.jTextField2.setText("");
                this.jTextField3.setText("");
                this.jTextField5.setText("");
                this.jComboBox1.setSelectedItem("");
                this.jTextField1.setEditable(true);
                this.jTextField2.setEditable(false);
                this.jTextField3.setEditable(false);
                this.jTextField5.setEditable(false);
            }
        }

    }//GEN-LAST:event_jButton4ActionPerformed


